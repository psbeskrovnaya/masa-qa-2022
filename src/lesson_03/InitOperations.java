package src.lesson_03;

public class InitOperations {
    public static void main(String[] args) {

        double a = 5.0;
        System.out.println("a = " + a);
        a += 2;
        System.out.println("a = " + a);
        a = a + 2;
        System.out.println("a = " + a);

        a = a - 2;
        System.out.println("a = " + a);
        a -= 2;
        System.out.println("a = " + a);

        a *= 5;
        System.out.println("a = " + a);

        a /= 5;
        System.out.println("a = " + a);



    }
}
