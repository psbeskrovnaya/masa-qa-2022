package src.lesson_03;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));


    }
}
