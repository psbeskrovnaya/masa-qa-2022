package src.lesson_03;

public class CompareOperators {
    public static void main(String[] args) {

        int a = 5;
        int b = 6;

        System.out.println("a > b " + (a > b));
        System.out.println("a < b " + (a < b));
        System.out.println("a != b " + (a != b));
        System.out.println("a == b " + (a == b));



    }
}
